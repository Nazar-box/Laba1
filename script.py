# Цілочисленні типи
age = 25  # Оголошення цілочисленної змінної
print(f"age, {type(age)} : {age}")  # Вивід змінної та її типу

# Дійсні числа
temperature = 36.6  # Оголошення змінної з плаваючою точкою
print(f"temperature, {type(temperature)} : {temperature}")

# Логічний тип
is_student = True  # Оголошення логічної змінної
print(f"is_student, {type(is_student)} : {is_student}")

# Рядковий тип
name = "John"  # Оголошення рядкової змінної
print(f"name, {type(name)} : {name}")

# Списки
numbers = [1, 2, 3, 4, 5]  # Оголошення списку
print(f"numbers, {type(numbers)} : {numbers}")

# Кортежі
coordinates = (10.5, 20.3)  # Оголошення кортежу
print(f"coordinates, {type(coordinates)} : {coordinates}")

# Словники
person = {"name": "Alice", "age": 30}  # Оголошення словника
print(f"person, {type(person)} : {person}")

# Множини
unique_numbers = {1, 2, 3, 4, 5}  # Оголошення множини
print(f"unique_numbers, {type(unique_numbers)} : {unique_numbers}")

# Арифметичні оператори
x = 10
y = 3

# Додавання
sum_result = x + y  # Оператор додавання
print(f"sum_result, {type(sum_result)} : {sum_result}")

# Віднімання
diff_result = x - y  # Оператор віднімання
print(f"diff_result, {type(diff_result)} : {diff_result}")

# Множення
mult_result = x * y  # Оператор множення
print(f"mult_result, {type(mult_result)} : {mult_result}")

# Ділення
div_result = x / y  # Оператор ділення (повертає float)
print(f"div_result, {type(div_result)} : {div_result}")

# Цілочисельне ділення
floor_div = x // y  # Оператор цілочисельного ділення
print(f"floor_div, {type(floor_div)} : {floor_div}")

# Залишок від ділення
mod_result = x % y  # Оператор залишку від ділення
print(f"mod_result, {type(mod_result)} : {mod_result}")

# Піднесення до степеня
power_result = x ** y  # Оператор піднесення до степеня
print(f"power_result, {type(power_result)} : {power_result}")

# Оператори порівняння
a = 5
b = 10

# Рівність
equal = a == b  # Оператор рівності
print(f"equal, {type(equal)} : {equal}")

# Нерівність
not_equal = a != b  # Оператор нерівності
print(f"not_equal, {type(not_equal)} : {not_equal}")

# Більше
greater = a > b  # Оператор "більше"
print(f"greater, {type(greater)} : {greater}")

# Менше
less = a < b  # Оператор "менше"
print(f"less, {type(less)} : {less}")

# Більше або рівно
greater_equal = a >= b  # Оператор "більше або рівно"
print(f"greater_equal, {type(greater_equal)} : {greater_equal}")

# Менше або рівно
less_equal = a <= b  # Оператор "менше або рівно"
print(f"less_equal, {type(less_equal)} : {less_equal}")

# Логічні оператори
p = True
q = False

# Логічне І
logical_and = p and q  # Оператор логічного І
print(f"logical_and, {type(logical_and)} : {logical_and}")

# Логічне АБО
logical_or = p or q  # Оператор логічного АБО
print(f"logical_or, {type(logical_or)} : {logical_or}")

# Логічне НЕ
logical_not = not p  # Оператор логічного НЕ
print(f"logical_not, {type(logical_not)} : {logical_not}")

# Оператори присвоєння
value = 15  # Просте присвоєння
value += 5  # Додавання з присвоєнням
print(f"value, {type(value)} : {value}")